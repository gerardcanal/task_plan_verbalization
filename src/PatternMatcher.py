class PatternMatcher:
    def __init__(self):
        pass

    @staticmethod
    def get_param_pattern(param_list_a, param_list_b):
        pattern = []
        for i, p in enumerate(param_list_a):
            try:
                j = param_list_b.index(p)
                pattern.append((i, j))
            except ValueError:
                pass  # Not in list
        return pattern
